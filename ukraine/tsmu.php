        <h5 class="card-title">UKRAINE</h5>
        <p class="card-text">
Ukraine is located in Central Eastern Europe. This country is Europe’s second largest by area and fifth largest by population. The history of Ukraine, similarly to other European countries, encompasses many diverse nations populating it overtime.<br><br>
The Trypillya Civilization, the Greeks, the Sarmatians and the Genovese all have left their traces on Ukraine’s land. The X-XI centuries were the heyday of a powerful eastern Slavic monarchy known as Kyivan Rus. It had extensive culture, trade and dynastical bonds with many European countries.<br><br>
A bright page of Ukraine’s history is the Cossacks Era of XIV century, when it existed as a sovereign Hetman Cossacks State. During the turbulent years of the early XX century Ukrainians attempted to proclaim their independence but the country became a part of the USSR. Despite the twists and turns of the history, Ukrainians had always cherished the dream to create a state of their own. That dream came true in 1991.<br><br>
Since then much has changed, as Ukraine overcame its Soviet past. The cities have changed significantly and business, culture and education are flourishing. The system of education traditionally has strong emphasis on fundamental sciences and now it is undergoing further major improvements to prepare young specialists for the demands of modern society an dworkplace, in 2005 Ukraine has adopted the Bologna education system of European countries.<br><br>

Life in Ukraine is dynamic and exciting. Ukrainians work hard but also have many opportunities for travel and cultural pursuits. They value their history and continue to build a beautiful and developed country.


</p>
      <hr>
       <h5 class="card-title">TERNOPIL</h5>
        <p class="card-text">
Ternopil, the city in Western Ukraine, was founded in 1540 as a Polish military strong hold and received city rights in 1548. Now it is the administrative centre of Ternopil region with area of 72 KM and population of 220 thousand people.<br><br>
The city is quiet and full of natural charms. It is a perfect place for students and young people. The center is a mix of architectural styles from XIX century till nowadays. The distinctive feature of Ternopil is its lake surrounded by parkland and recreational areas.
Ternopil region is famous for its fertile lands, where wheat, canola and sugar beets are grown. Its industries include food processing, textile, clothes, synthetic leather production and manufacturing of electrical components and building materials.<br><br>
From Ternopil one can easily reach some of the most beautiful natural areas, such as Carpathian Mountains and Dnister River; historic sites (castles and the Lviv city center) as well as the other cities of Ukraine and neighbouring countries.<br><br>
As an educational hub, Ternopil is a home for 4 universities as well as numerous institutes, colleges and schools and is called a students’ city. Students are a large part of Ternopil’s population. International students make it a city of diverse cultures. Most of the city’s educational facilities and campuses are situated with in the walking. 
Ternopil is a place where one can receive high quality education while enjoying life in a pleasant and peaceful environment.


