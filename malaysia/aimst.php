Integrated Preclinical with Year 1 courses which features Basic Medical Sciences, Society & Health, Biostatistics & Ethics in Medicine coupled with short interface with the hospital. Year 2 courses are dedicated to Cardiovascular Systems, Respiratory Systems and Gastrointestinal Systems, Renal Systems, Central Nervous Systems and Endocrine & Reproductive Systems<br><br>
<b>Phase 2</b><br>
The programme covers the Clinical Years 3 & 4 and is designated as Junior Clerkship. Year 3 covers Medicine, Surgery, Obstetrics & Gynaecology and Paediatrics. There will also be integrated clinical teaching of Diabetes. Students are also required to complete one SSM in Year 3. Year 4 is essentially devoted to the coverage of Clinical Sub-specialties, Forensic Medicine and Clinical Laboratory Sciences. During this phase students will actively engage in PBL sessions, symposia, clinical classes and self-directed learning<br><br>
<b>Phase 3</b><br>
Senior Clerkship years are where the students will consolidate the knowledge gained in Phases 1 & 2. Students will revisit and strengthen the skills in all the four major areas: Medicine, Surgery, Obstetrics & Gynaecology and Paediatrics<br><br>
